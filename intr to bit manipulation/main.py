num1= 10
num2=4
#AND OPERATOR
print("Num1 AND Num2=",num1&num2)
#OR OPERATOR
print("Num1 OR Num2=",num1|num2)
#XOR OPERATOR
print("Num1 XOR Num2=",num1^num2)
#NOT OPERATOR
print("Not Num1=",~num1)
#LEFT SHIFT OPERATOR
print("Num1<<2=",num1<<2)
#10011 <<2 =1001100 add 2 zeros at the end
#RIGHT SHIFT OPERATOR
print("Num1>>2=",num1>>2)
#10011 >>2 =100 replace the last digit with zeros